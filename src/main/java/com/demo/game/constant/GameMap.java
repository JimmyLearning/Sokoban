	/**
	 * total of level
	 *
	 * @return
	 */
	public static int getTotalLevel() {
		return MAP.length;
	}

	/**
	 * get the MAP data of level
	 *
	 * @param level
	 * @return
	 */
	public static int[][] getMap(int level) {
		return copyMap(level);
	}

	private static int[][] copyMap(int level) {
		if (level <= 0 || level > getTotalLevel()) {
			throw new MapException("GameMap.copyMap(); level number is invalid");
		}
		int mapWidth = MAP[level - 1].length;
		int mapHeight = MAP[level - 1][0].length;
		int[][] copy = new int[mapWidth][mapHeight];
		for (int i = 0; i < mapWidth; i++) {
			copy[i] = Arrays.copyOf(MAP[level - 1][i], mapHeight);
		}
		return copy;
	}
